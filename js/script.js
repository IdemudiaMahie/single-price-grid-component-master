// The code below was gotten from chatGPT. It5 is a simplified version of my code above. I actually prefer it to mine. The only slight difference is that chatGPT gave me as one that loops to return 6 random characters as opposed to the 8 i set mine to generate. By just setting the condition in the for loop to be i less than 8 and not 6, it becomes equal.

const button = document.querySelector('button');
const article = document.querySelector('article');
const sections = article.querySelectorAll('section');

button.addEventListener('click', () => {
  sections.forEach((section) => {
    section.style.backgroundColor = getRandomColor();
  });
});

function getRandomColor() {
  const hexCharacters = '0123456789abcdef';
  let color = '#';
  for (let i = 0; i < 6; i++) {
    color += hexCharacters[Math.floor(Math.random() * 16)];
  }
  return color;
}
